            <h2 style="color: #66d; text-align: left; margin-top: -4px;">My heart on user experience:</h2>
            <p>Design to me is more than just making beautiful work. It's a process to connect with people: understanding who they are, what their needs and frustrations are, and creating something that matters to them. Every design should be a tool that empowers and resonates with the person it's for. UX matters to me because people matter to me — I want to live my life making things better and more relevant for people, and I'm happy putting in the thought, care and effort needed to get results.</p>
