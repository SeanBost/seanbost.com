            <img src="media/aamd/calendar.png" width="100%" alt="Two basic social media graphics featuring pictures of detailed cars">
            <h2 style="color: #66d;">Internal Onboarding Graphic</h2>
            <p>A key challenge for us was poor detailer retention, especially with newer
                hires. One of the primary reasons new detailers 
                would leave would be disputes over pay, specifically when they would receive their first
                check and what jobs it would include.</p>
            <p>To alleviate some confusion, I spoke with our detailer support team, and we conceptualized a few ways we can communicate better. 
                I then made this graphic breaking down the admittedly complex detailer pay periods. I also created and edited it into an onboarding 
                video along with a brief narrated explanation from the company founder.</p>
                <p>We immediately saw a decrease in previously frequent conflicts
                regarding when a new detailer would get paid, and with the help of other measures an increase in detailer retention overall.</p>
        </div>
        <div class="content-left">
            <h2 style="color: #66d;">Building Customer Confidence Via Social Media</h2>
            <p>When I joined AAMD building our reputation was a high priority, with many pontential customers questioning the authenticity of our brand. 
                In addition to setting up systems to
                improve our CX and to encourage positive reviews, I also
                introduced a categorical social media content calendar, with a heavy focus on brand building posts.</p>
            <p>Regular posts such as this one, tailored to convey trust and expertise, would show alongside CTA-oriented copy I'd write. 
                Compared to prior posts, these had much higher engagement rates and helped
                funnel potential customers towards sales.
            </p>
        </div>
