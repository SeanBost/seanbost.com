            <h1 style="text-align: center; color: #d44; font-size: 350%;">All American Mobile Detailing</h1>
            <p>While my role at AAMD extended far beyond creating content and assets,
                the media I did create working alongside our other teams played an important role in improving our 
                customer experience, sales, brand, and customer & detailer retention.</p>
        </div>
        <div class="content-left">
            <h2 style="color: #66d;">Automated QA Email Flows</h2>
            <p>As we needed more data on detailer performance yet lacked the resources to follow up each job over phone, 
                I introduced automated follow-up emails through Mailchimp that collected quantitative and qualitative customer insights.</p>
            <p>In addition to aggregating customer responses for internal use, their sentiments either flagged them for a support member to
                contact to address concerns, or asked them to leave a review and placed them into a targeted marketing email flow I built
                to encourage repeat customers through holiday deals or loyalty incentives.
            </p>
            <p>Sales from prior customers via email costed less than a tenth of what new customers did through Google Ads on average.</p>
        </div>
        <div class="content-right" style="padding-left: 30px; padding-right: 30px;">
