            <h2 style="color: #66d;">Generic Content Template</h2>
            <p>I was often stretched for time, and could not spend much of it on content
                generation. This lead me to identify and test content patterns and concepts 
                to see how they performed, with an emphasis on engagement and link clicks. Eventually, I was able to build a generic content and
                brand style guide based on what got results and was easily repeatable. I was then able to consistently make successful if generic 
                graphics rapidly when my hands and time were tied up with higher priority projects.</p>
        </div>
        <div class="content-full" style="margin-top: -30px;">
            <h2 style="color: #d44;">Optimizing Detailer Hiring</h2>
            <p>The winter I started the largest issue we faced was attracting detailing talent with very limited resources.
                We were haphazardly placing hiring ads in various cities without structure.
                I built a spreadsheet with every metro area deemed viable
                along with dates to post hiring ads in them. We cycled through all of them once and our most important targets twice every few weeks.
                This ensured we weren't wasting ads where we had already tapped all talent and weren't
                leaving out any key potential areas for business.</p>
            <p>I also added cells for inputting a metro's application count upon ad closure and for how many of them were viable.
                Formulas automatically determined daily and weekly app totals, average applicant and location quality statistics, and more. 
                I used this data to deprioritize or remove underperforming areas, A/B test different copy and tactics, and iteratively improve our strategies.</p>
            <p>It also let us collect quantitative feedback on how different compensation packages we offered impacted recruitment
                quality and quantity at a regional level. We regularly used this data alongside retention stats to perform cost benefit analyses on various detailer
                compensation measures.</p>
        </div>
        <div class="content-center" style="margin-top: -60px;">
            <p>While most numbers always remained volatile, <b style="color: #d33;">in the span of several months my initiatives had direct impact on doubling the number of detailers in our employment</b>
                and also greatly increased the total areas we served. I also scaled
                our marketing coverage without significant change in our CPA through similar explorative and iterative methods. <b style="color: #d33;">As detailer count directly
                correlated with total revenue, this stabilized our bottom line and provided us with clarity for further growth</b>.</p>
