        <div class="content-left">
            <h2 style="color: #66d;">Holiday Special Graphics</h2>
            <p>Through analyzing our job bookings and consulting our sales team, I uncovered that our most
                effective way sell to existing customers was through limited offer promotions and special
                occasions. I built a marketing calendar with
                lead-up and flash promotions planned for every holiday we could target. In the gaps between holidays I built incentives for gift cards,
                detail anniversaries, and friends-and-family deals.</p>
        </div>
        <div class="content-right" style="padding-left: 30px; padding-right: 30px;">
